using Amazon.PowerShell.Common;
using Amazon.Runtime;
using Amazon.S3;
using Amazon.S3.Model;

namespace Amazon.PowerShell.Cmdlets.S3
{
    /// <summary>
    /// Returns the versioning state of a bucket.
    /// </summary>
    [Cmdlet("Get", "S3BucketVersioning")]
    [OutputType("Amazon.S3.Model.S3BucketVersioningConfig")]
    [AWSCmdlet("Invokes the GetBucketVersioning operation against Amazon Simple Storage Service.", Operation = new[] {"GetBucketVersioning"})]
    [AWSCmdletOutput("Amazon.S3.Model.S3BucketVersioningConfig",
        "This cmdlet returns a S3BucketVersioningConfig object.",
        "The service call response (type Amazon.S3.Model.GetBucketVersioningResponse) can also be referenced from properties attached to the cmdlet entry in the $AWSHistory stack."
    )]
    public class GetS3BucketVersioningCmdlet : AmazonS3ClientCmdlet, IExecutor
    {
        /// <summary>
        /// <para>
        /// The name of the bucket to be queried.
        /// </para>
        /// </summary>
        [System.Management.Automation.Parameter(Position = 0, ValueFromPipelineByPropertyName = true, ValueFromPipeline = true)]
        public System.String BucketName { get; set; }
        
        
        protected override void ProcessRecord()
        {
            base.ProcessRecord();
            
            var context = new CmdletContext
            {
                Region = this.Region,
                Credentials = this.CurrentCredentials
            };
            
            context.BucketName = this.BucketName;
            
            var output = Execute(context) as CmdletOutput;
            ProcessOutput(output);
        }
        
        #region IExecutor Members
        
        public object Execute(ExecutorContext context)
        {
            var cmdletContext = context as CmdletContext;
            // create request
            var request = new Amazon.S3.Model.GetBucketVersioningRequest();
            
            if (cmdletContext.BucketName != null)
            {
                request.BucketName = cmdletContext.BucketName;
            }
            
            CmdletOutput output;
            
            // issue call
            var client = Client ?? CreateClient(context.Credentials, context.Region);
            try
            {
                var response = client.GetBucketVersioning(request);
                Dictionary<string, object> notes = null;
                object pipelineOutput = response.VersioningConfig;
                output = new CmdletOutput
                {
                    PipelineOutput = pipelineOutput,
                    ServiceResponse = response,
                    Notes = notes
                };
            }
            catch (Exception e)
            {
                output = new CmdletOutput { ErrorResponse = e };
            }
            
            return output;
        }
        
        public ExecutorContext CreateContext()
        {
            return new CmdletContext();
        }
        
        #endregion
        
        
        internal class CmdletContext : ExecutorContext
        {
            public System.String BucketName { get; set; }
        }
        
    }
}
